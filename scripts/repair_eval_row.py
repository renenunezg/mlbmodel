"""Repair the model_evaluation / model_calibration / model_edge_buckets rows
for a single pre-cutover date.

The freeze guard in backend/evaluate_model.py refuses to write rows for any
date before V1_CUTOVER_DATE. That's the desired behavior 99% of the time. If
a historical row ever needs to be regenerated from `model_outputs_season_v1_archive`
(e.g. a v1 backfill exposed corrected predictions, or a re-import wiped a row),
run this script to bypass the freeze for that one date.

Usage:
    python -m scripts.repair_eval_row --date 2026-05-11
    python -m scripts.repair_eval_row --date 2026-04-15 --dry-run
"""
from __future__ import annotations

import argparse
import datetime
import sys

import numpy as np
import pandas as pd
from sqlalchemy import MetaData, text

from backend import evaluate_model, strategy
from backend.db import engine
from backend.evaluate_model import (
    _build_bet_ledger,
    _compute_base_row,
    _write_calibration,
    _write_edge_buckets,
    _write_evaluation_row,
)
from backend.metrics import (
    calibration_curve,
    equity_curve_from_ledger,
    financial_summary,
    hit_rate_by_edge_bucket,
    probabilistic_summary,
    regression_summary,
    segment_summary,
)


def _load_eval_df(d: datetime.date) -> pd.DataFrame:
    sql = text(
        """
        SELECT m.*, g.home_team, g.away_team, g.home_score, g.away_score
        FROM model_outputs_season_v1_archive m
        JOIN games g ON g.game_pk = m.game_pk
        WHERE m.date::date = :d
          AND g.status = 'Final'
          AND g.home_score IS NOT NULL
          AND g.away_score IS NOT NULL
        """
    )
    with engine.connect() as conn:
        df = pd.read_sql(sql, conn, params={"d": d})
    if df.empty:
        return df

    df["winning_team"] = np.where(df["home_score"] > df["away_score"], df["home_team"], df["away_team"])
    df["actual_runs"] = np.where(df["team"] == df["home_team"], df["home_score"], df["away_score"])
    df["actual_margin"] = np.where(
        df["team"] == df["home_team"],
        df["home_score"] - df["away_score"],
        df["away_score"] - df["home_score"],
    )
    df["game_total"] = df["home_score"] + df["away_score"]
    df["actual_win"] = (df["team"] == df["winning_team"]).astype(int)
    df["pred_win"] = (df["win_prob"] > 0.5).astype(int)
    df["game_date"] = pd.to_datetime(df["date"]).dt.normalize()
    return df


def _compute_day_metrics(eval_df: pd.DataFrame):
    ledger = _build_bet_ledger(eval_df)
    if not ledger.empty:
        ledger["date"] = pd.to_datetime(ledger["date"])

    window_df = eval_df
    if not ledger.empty:
        window_dates = set(window_df["game_date"].dt.date)
        window_ledger = ledger[ledger["date"].dt.date.isin(window_dates)]
    else:
        window_ledger = pd.DataFrame(columns=ledger.columns if not ledger.empty else [])

    base = _compute_base_row(window_df, window_ledger)
    y_true = window_df["actual_runs"].values.astype(float)
    y_pred = window_df["expected_runs"].values.astype(float)
    probs = window_df["win_prob"].values.astype(float)
    outcomes = window_df["actual_win"].values.astype(float)

    reg = regression_summary(y_true, y_pred)
    prob = probabilistic_summary(probs, outcomes, y_pred, y_true)
    fin = financial_summary(window_ledger) if not window_ledger.empty else {}
    seg = segment_summary(window_ledger)
    eq = equity_curve_from_ledger(window_ledger)
    equity_end = float(eq["equity"].iloc[-1]) if not eq.empty else 1.0

    metric = {**reg, **prob, **fin, **seg, "equity_end_units": equity_end}
    cal = calibration_curve(probs, outcomes)
    buckets = hit_rate_by_edge_bucket(window_ledger)
    return base, metric, cal, buckets


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--date", required=True, help="YYYY-MM-DD")
    ap.add_argument("--dry-run", action="store_true", help="Show what would be written; do not commit")
    args = ap.parse_args()

    d = datetime.date.fromisoformat(args.date)
    if d >= strategy.V1_CUTOVER_DATE:
        print(f"refuse: {d} is on/after the v1 cutover ({strategy.V1_CUTOVER_DATE}). "
              f"Post-cutover dates should be regenerated by the normal evaluate_model run.")
        return 2

    eval_df = _load_eval_df(d)
    if eval_df.empty:
        print(f"refuse: no v1-archive predictions joined to Final games for {d}.")
        return 2

    base, metric, cal, buckets = _compute_day_metrics(eval_df)
    print(f"computed metrics for {d}:")
    print(f"  predictions={base['total_predictions']}  accuracy={base['total_accuracy']}  "
          f"ml_acc={base['ml_accuracy']}  runs_mae={base['average_total_diff']}")

    if args.dry_run:
        print("\n(dry-run; nothing written)")
        return 0

    md = MetaData()
    md.reflect(bind=engine, only=["model_evaluation"])
    valid = {c.name for c in md.tables["model_evaluation"].columns}
    metric_filtered = {k: v for k, v in metric.items() if k in valid}

    original = strategy.V1_CUTOVER_DATE
    strategy.V1_CUTOVER_DATE = datetime.date(1900, 1, 1)
    evaluate_model.V1_CUTOVER_DATE = datetime.date(1900, 1, 1)
    try:
        with engine.begin() as conn:
            conn.execute(text("DELETE FROM model_calibration WHERE date=:d"), {"d": d})
            conn.execute(text("DELETE FROM model_edge_buckets WHERE date=:d AND eval_window='day'"), {"d": d})
        _write_evaluation_row(d, "day", base, metric_filtered)
        _write_calibration(d, cal)
        _write_edge_buckets(d, "day", buckets)
    finally:
        strategy.V1_CUTOVER_DATE = original
        evaluate_model.V1_CUTOVER_DATE = original

    print(f"  repaired model_evaluation / model_calibration / model_edge_buckets for {d}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
